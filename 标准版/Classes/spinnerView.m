#import "spinnerView.h"
#import "Common.h"

@implementation spinnerView
+(void)startSpinner:(UIView*)view{

	[commonSpinner setCenter:CGPointMake(view.frame.size.width / 2.0, view.frame.size.height / 2.0)]; // I do this because I'm in landscape mod
	[commonSpinner startAnimating];
	[view addSubview:commonSpinner];
	//[view bringSubviewToFront:gspinner];
	//gspinner 
	
}
+(void)stopSpinner{
	[commonSpinner stopAnimating];
	[commonSpinner removeFromSuperview];
}
@end
